	/**
	 * This class is used to hold bound pairs in _ARRAY objects.
	 *
	 */
	public final class _BOUNDS {
		/**
		 * An array upper bound
		 */
		final public int LB;
		/**
		 * An array ELEMENTS size
		 */
		final public int SIZE;

		/**
		 * Create an array bound pair object
		 * @param LB Lower bound
		 * @param UB Upper bound
		 */
		public _BOUNDS(final int LB, final int UB) {
			if (LB > UB)
				throw new _SimulaRuntimeError("Lower bound(" + LB + ") > upper bound(" + UB + ")");
			this.LB = LB;
			SIZE = UB - LB + 1;
		}

		@Override
		public String toString() {
			return ("" + LB + ':' + (LB + SIZE - 1));
		}
	}


