	/**
	 * Text value relation
	 * @param left left hand side
	 * @param right right hand side
	 * @param code relational code
	 * @return true if relation holds
	 */
	private static boolean TXTREL(_TXT left, _TXT right, int code) {
		int i; // Loop index.
		int dif; // Difference between lengths.
		int lng; // Length of common parts.
		if (left == null)
			left = NOTEXT;
		if (right == null)
			right = NOTEXT;
		lng = right.LENGTH;
		dif = lng - left.LENGTH;
		if (dif != 0) {
			if (code == 2)
				return (false);
			if (code == 5)
				return (true);
			if (dif > 0)
				lng = left.LENGTH;
		}
		i = 0;
		while (i < lng) {
			int rightChar = right.OBJ.MAIN[right.START + i];
			int leftChar = left.OBJ.MAIN[left.START + i];
			if (rightChar != leftChar) {
				dif = rightChar - leftChar;
				break;
			}
			i = i + 1;
		}
		switch (code) {
		case 1:
			return (0 < dif);
		case 2:
			return (0 == dif);
		case 3:
			return (0 <= dif);
		case 4:
			return (0 > dif);
		case 5:
			return (0 != dif);
		case 6:
			return (0 >= dif);
		default:
			throw new _SimulaRuntimeError("Internal Error");
		}
	}

